private void cadastrar() {

        ApartamentoDao mdao = new ApartamentoDao();
        Apartamento mod = new Apartamento();

        mod.setapnumero(jTdisponibildade.getText());
        mod.setapandar(jTnumero.getText());
        mod.setdispo(jTandar.getText());
        

        Bloco fab = (Bloco) jcbloco.getSelectedItem();

        mod.setbloco(fab);

        jTnumero.setText("");
        jTdisponibildade.setText("");
        jTandar.setText("");
        
        jcbloco.setSelectedIndex(0);
        jTnumero.requestFocus();

        try {
            mdao.cadastrar(mod);

            System.out.println("Salvo com Sucesso");
        } catch (SQLException ex) {
            System.out.println("NÃ£o salvou:"+ ex);;
        }

    }

public void preencherComboApartamento(JComboBox combo) {
         
        BlocoDao fdao = new BlocoDao();

        try {
            List<Bloco> lista = fdao.listarTodos();

            for (Bloco fab : lista) {
                combo.addItem(fab);
            }
        } catch (SQLException ex) {

            System.out.println("Erro ao recuperar");
        }

    }

